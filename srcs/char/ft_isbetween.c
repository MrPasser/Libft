int ft_isbetween(int x, int m, int n)
{
	return ((((x) - ~0UL / 255 * (n)) & ~(x) & ((x) & ~0UL / 255 * 127) + ~0UL /
										255 * (127 - (m))) & ~0UL / 255 * 128);
}