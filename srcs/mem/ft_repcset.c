unsigned long	ft_repcset(int c)
{
	unsigned long cc;

	if ((cc = (unsigned char)c) != 0)
	{
		cc |= cc << 8;
		cc |= cc << 16;
		cc |= cc << 32;
	}
	else
		cc = 0x00;
	return (cc);
}
